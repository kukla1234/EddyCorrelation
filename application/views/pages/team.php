                        Former Ph.D Student, 2013
                    </p>
                    <p>
                        While the drivers of metabolism in aquatic ecosystems are well known and include nutrients, 
                        sunlight, temperature, organic matter inputs, and water flow or mixing, our ability to 
                        quantify the effects of these drivers on metabolism is limited. This limits our understanding 
                        of the impacts of these drivers on water quality and aquatic carbon transformation. My 
                        research focuses on a novel hydrodynamic technique, eddy covariance, which can be used to 
                        determine the flux of oxygen, heat, and salt in an aquatic ecosystem at a high temporal 
                        resolution and without a gas transfer velocity correction. The calculated fluxes which result 
                        can improve our predictions of an aquatic ecosystem’s response to environmental change.
                    </p>
                    <p>
                        An abundant source of nutrients to freshwater and shallow marine ecosystems is groundwater. 
                        Because over-enrichment of surface waters with nutrients leads to water quality degradation 
                        and habitat loss, I have also worked with seepage meters, radioisotopic tracers, thermal 
                        imagery, and salt and heat fluxes calculated with the eddy covariance technique to quantify 
                        sources of groundwater discharge to surface waters. The delivery of groundwater nutrients to 
                        surface water at some locations is controlled by microbial activity in sediments. By improving 
                        our understanding of these processes we can improve the application of our effort to preserving 
                        water quality and aquatic habitats.
                    </p>
