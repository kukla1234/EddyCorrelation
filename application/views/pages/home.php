            <p>Welcome to the Aquatic Eddy Covariance Research Lab at the University of Virginia.</p>

            <p> We work with the eddy covariance technique, also known as the eddy correlation technique, for underwater flux measurements in three main areas:</p>

            <ol class='berg_indent-block'>
                <li>
                    In situ applications in various aquatic environments to address research questions on carbon and nutrient cycling.
                </li>

                <li>
                    Sensor development and flux calculation approaches to further advance the technique.
                </li>

                <li>
                    Training and education.
                </li>
            </ol>

            <p>
                The aquatic eddy covariance technique was adapted from the atmospheric boundary layer to the benthic environment by Peter Berg and the <a href="http://www.mpi-bremen.de/en/"> Max Planck Institute for Marine Microbiology, Germany</a>. The first proof of concept paper focusing on oxygen fluxes for different benthic ecosystems was published in 2003 (Berg et al. 2003), and since then an increasing number of groups have adopted the approach.
            </p>
        </div>
