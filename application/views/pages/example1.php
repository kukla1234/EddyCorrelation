        <div class='berg_image-container berg_align-center'>
          <img src="/berg/img/data/PlotWebSite1.png" alt="Data Example 1" class="berg_carousel-image">
            <div class='berg_image-caption berg_rounded_8'>
              <p>
                High-quality eddy covariance data through 17 hours measured over permeable sand in the Gulf of 
                Mexico, Florida. Water depth varied from 1 to 1.5 m, and sediment permeability was 1.3 10-11 m2. 
                Raw data were recorded in 15 min data segments that included a short pause at the end.  
              </p>
                
              <p>
                First panel: Three velocity components (x, y, z) and mean current velocity varying between 1 
                and 5 cm s-1. Most of the variation in velocity represents wave action. Second panel: Oxygen 
                concentration measured with a fast-responding micro-electrode and an oxygen meter. The latter 
                was used for calibration. Third panel: Cumulative oxygen fluxes with linear trends indicating a 
                strong consistent flux signal in the data. Fourth panel: Oxygen fluxes, one for each 15 min data 
                segment, and light measured over the sand. Negative fluxes represent an uptake.
              </p>

              <p>
                Note the clear day-night-day cycle (Fourth panel). The four last fluxes of extreme size were 
                artificially induced by people wading upstream from the eddy covariance instrument. Night 
                time oxygen uptake averaged -368 ± 21 mmol m-2 day-1 (SE, n = 45) which is almost 4 times 
                larger than the flux of -98 ± 21 mmol m-2 day-1 (SE, n = 5) measured concurrently in in situ 
                chambers. This difference was likely caused by extensive flushing of the permeable sediment 
                by current flow, oscillating wave-generated flow, and oscillating pressure at the sediment 
                surface. Existing chamber technology cannot fully reproduce these naturally occurring drivers 
                of porewater exchange. For further details on the data, see 
                <a href="berg/journals/Berg%20and%20Huettel%202008%20Oceanography.pdf">Berg and Huettel (2008)
                </a>. The 16 Hz data (time, vx, vy, vz, O2) for this example can be downloaded 
                <a href="/berg/data/BergData1.zip">here</a>.
              </p>
