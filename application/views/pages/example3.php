        <div class='berg_image-container berg_align-center'>
          <img src="/berg/img/data/PlotWebSite3.png" alt="Data Example 3" class="berg_carousel-image">
            <div class='berg_image-caption berg_rounded_8'>
              <p>
                High-quality eddy covariance data measured in a test deployment of a new robust proto-type oxygen 
                sensor. The data covering 22 hours were measured over permeable sand at 10 m depth in the Florida 
                Keys. Raw data were recorded in one long continuous data sequence.  
              </p>
                
              <p>
                First panel: Three velocity components (x, y, z) and mean current velocity varying between 1 and 14 
                cm s-1. Some of the variation in velocity represents wave action. 
              </p>

              <p>
                Second panel: Oxygen concentration measured with the fast-responding oxygen proto-type sensor and a 
                stable optode. The latter was used for calibration. The proto-type sensor follows very closely the 
                concentration recorded by the stable optode. 
              </p>


              <p>
                Third panel: Cumulative oxygen fluxes, one for each 15 min data segment, with clear linear trends 
                indicating a strong consistent flux signal in the data. 
              </p>

              <p>
                Fourth panel: Oxygen fluxes, one for each 15 min data segment, and light (PAR) measured over the 
                permeable sand. Positive fluxes represent a release. 
              </p>

              <p>
                Note the tight correlation between oxygen flux and light (Fourth panel), and also the spike in flux 
                right before midnight (24 hour) which was caused by an abrupt increase in current velocity (First panel), 
                from 7 to 14 cm s-1 over a 5 min period. 
              </p>

              <p>
                The “clean” oxygen concentration data (Second panel) are recorded by the proto-type sensor without drift, 
                spikes, sudden jumps, or any other abnormalities, and are superior to traditional data recorded with regular 
                Clark-type microelectrodes. This makes flux extractions more straight forward, and also increases the success 
                rate of data collection to close-to 100% of the deployment time.
              </p>
